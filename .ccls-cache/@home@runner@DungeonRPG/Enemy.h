//base class
class Enemy {
  protected:
    int hp;
    int atk;
    int coin;


  public:
    //default constructor
    Enemy() {
        srand(time(0)); // Seed the random number generator
        hp = rand() % 200 + 700;  // Random HP between 700 and 899
        atk = rand() % 50 + 25; // Random ATK between 25 and 74
        coin = rand() % 2500 + 240; // Random coin between 240 and 2739
    }
    //overload constructor
    Enemy(int higherHealth){
      srand(time(0)); // Seed the random number generator
      hp = rand() % 500 + 1000; // Random HP between 1000 and 1499
      atk = rand() % 50 + 25; // Random ATK between 25 and 74
      coin = rand() % 2500 + 240; // Random coin between 240 and 2739
    }

    virtual ~Enemy() {} //destructor method

    virtual void printFace() = 0;
    virtual void attack() = 0;

    //get the enemy's stats
    int getHP() const { return hp; }
    int getATK() const { return atk; }
    int getCoin() const { return coin; }
};

